#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int a;
	int b;

	a = 0;
	if (s != NULL && f != NULL)
	{
		b = ft_strlen(s);
		while (a < b)
		{
			(*f)(s);
			s++;
			a++;
		}
	}
}
