#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i;
	int b;

	i = 0;
	b = 0;
	if (!(s1) || !(s2))
		return (0);
	while (s1[i] == s2[b] && s1[i] != '\0' && s2[b] != '\0')
	{
		i++;
		b++;
	}
	if ((s1[i] - s2[b]) == 0)
		return (1);
	else
		return (0);
}
