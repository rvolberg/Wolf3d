#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int len;

	i = 0;
	if (!(s))
		return ;
	len = ft_strlen(s);
	while (i < len)
	{
		s[i] = '\0';
		i++;
	}
}
